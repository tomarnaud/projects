char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	a;
	
	a = c;
	i = -1;
	while(s[++i])
		if (s[i] == a)
			return((char *)&s[i]);
	if (s[i] == a)
		return((char *)&s[i]);
	return (0);
}
